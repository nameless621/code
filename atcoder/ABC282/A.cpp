#include <bits/stdc++.h>
using namespace std;
#include <atcoder/all>
using namespace atcoder;


//    A - Generalized ABC
//    https://atcoder.jp/contests/abc282/tasks/abc282_a



int main() {
    int K;
    cin >> K;
    for(char c = 'A'; c < 'A' + K; c++) {
        cout << c;
    }
    cout << endl;
}